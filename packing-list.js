const packingList = ["tent", "bug spray", "sleeping bag"];

// use for loop
console.log("Kitchen stuff to pack:");
for (let i = 0; i < packingList.length; i++) {
  console.log(packingList[i]);
}

// use while loop
console.log("Kitechen stuff to pack:");
let i = 0;
while (i < packingList.length) {
    console.log(packingList[i]);
    i++;
}
